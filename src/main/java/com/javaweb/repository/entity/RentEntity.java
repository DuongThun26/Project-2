package com.javaweb.repository.entity;

public class RentEntity {
	private int id, rent, buildingtype_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getRent() {
		return rent;
	}

	public void setRent(int rent) {
		this.rent = rent;
	}

	public int getBuildingtype_id() {
		return buildingtype_id;
	}

	public void setBuildingtype_id(int buildingtype_id) {
		this.buildingtype_id = buildingtype_id;
	}
	
	
}
